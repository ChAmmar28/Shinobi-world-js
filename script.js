function cardGener(arr) {
  const res = arr
    .map(
      (item) =>
        `
      <div class="card">
        <img src='${item.image} alt='carecter' class="img"> 
        <div class="cardText">
            <h3>${item.name}</h3>
            <h4>${item.rang}</h4>
            <h4>${item.vallage}</h4>
            <h5>${item.gender}</h5>
        </div>
      </div>
      `
    )
    .join("");
  return res;
}

